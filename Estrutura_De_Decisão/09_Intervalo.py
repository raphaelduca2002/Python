"""9.	Faça um algoritmo que receba um número e diga se este número está no intervalo entre 100 e 200."""

numero = float(input("Informe um número: "))

if 100.00 <= numero <= 200.00:
    print(f"O número informado {numero} está dentro de 100-200.")
else:
    print(f"O número informado {numero} não está dentro de 100-200.")
