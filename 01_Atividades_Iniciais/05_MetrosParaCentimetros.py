"""5. Desenvolva um algoritmo que receba um valor em metros e converta para centímetros, depois apresente o resultado
em centímetros."""


#  Entrada de Dados

metros = float(input("Insira um valor em Metros: "))

#  Processamento de Dados

metros = metros * 100

#  Saída de Dados

print(f"O Valor em centimetros é de: {metros} CM")
